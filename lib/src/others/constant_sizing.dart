/// A collection of sizing constants for consistent UI layouts across the app.
class ConstantSizing {
  // ---------------------------------------------------------------------------
  // Font Sizes
  // ---------------------------------------------------------------------------
  /// Extra small font size, often used for captions.
  static const double fontSizeTiny = 10.0;

  /// Small font size, for less prominent text.
  static const double fontSizeSmall = 12.0;

  /// Default body text size.
  static const double fontSizeBody = 14.0;

  /// Medium font size, for regular text content.
  static const double fontSizeMedium = 16.0;

  /// Large font size, suitable for subheadings.
  static const double fontSizeLarge = 20.0;

  /// Extra large font size, often used for prominent headings.
  static const double fontSizeExtraLarge = 24.0;

  /// Huge font size, for major headlines.
  static const double fontSizeHuge = 32.0;

  // ---------------------------------------------------------------------------
  // Spacing (Margins & Paddings)
  // ---------------------------------------------------------------------------
  /// Extra small spacing for tight layouts.
  static const double spaceExtraSmall = 4.0;

  /// Small spacing, ideal for minor gaps.
  static const double spaceSmall = 8.0;

  /// Medium spacing, standard gap between elements.
  static const double spaceMedium = 16.0;

  /// Large spacing, used for larger separations.
  static const double spaceLarge = 24.0;

  /// Extra large spacing, for emphasizing separations.
  static const double spaceExtraLarge = 32.0;

  /// Huge spacing for generous layout separations.
  static const double spaceHuge = 40.0;

  // ---------------------------------------------------------------------------
  // Divider Thickness
  // ---------------------------------------------------------------------------
  /// Thin divider line, subtle separation.
  static const double dividerThin = 1.0;

  /// Thick divider line, for more pronounced separation.
  static const double dividerThick = 2.0;

  // ---------------------------------------------------------------------------
  // Border Radii
  // ---------------------------------------------------------------------------
  /// Small border radius for slightly rounded corners.
  static const double borderRadiusSmall = 4.0;

  /// Medium border radius for standard rounding.
  static const double borderRadiusMedium = 8.0;

  /// Large border radius for more prominent rounded corners.
  static const double borderRadiusLarge = 16.0;

  /// Radius to create circular shapes (often used with square dimensions).
  static const double borderRadiusCircle = 50.0;

  // ---------------------------------------------------------------------------
  // Icon Sizes
  // ---------------------------------------------------------------------------
  /// Small icon size for minor UI elements.
  static const double iconSizeSmall = 16.0;

  /// Medium icon size, a common choice for most icons.
  static const double iconSizeMedium = 24.0;

  /// Large icon size for primary actions or emphasis.
  static const double iconSizeLarge = 32.0;

  /// Extra large icon size, used in prominent displays.
  static const double iconSizeExtraLarge = 48.0;

  // ---------------------------------------------------------------------------
  // Button Dimensions
  // ---------------------------------------------------------------------------
  /// Small button height, for compact controls.
  static const double buttonHeightSmall = 36.0;

  /// Medium button height, standard for most buttons.
  static const double buttonHeightMedium = 48.0;

  /// Large button height, for primary or call-to-action buttons.
  static const double buttonHeightLarge = 56.0;

  /// Horizontal padding inside buttons.
  static const double buttonPaddingHorizontal = 16.0;

  /// Vertical padding inside buttons.
  static const double buttonPaddingVertical = 8.0;

  // ---------------------------------------------------------------------------
  // App Bar & Navigation
  // ---------------------------------------------------------------------------
  /// Standard app bar height.
  static const double appBarHeight = 56.0;

  // ---------------------------------------------------------------------------
  // Cards and Containers
  // ---------------------------------------------------------------------------
  /// Standard elevation for card widgets.
  static const double cardElevation = 2.0;

  /// Standard image corner radius.
  static const double imageRadius = 8.0;

  // ---------------------------------------------------------------------------
  // List & Form Elements
  // ---------------------------------------------------------------------------
  /// Standard height for list items.
  static const double listItemHeight = 56.0;

  /// Standard height for form fields.
  static const double formFieldHeight = 56.0;

  // ---------------------------------------------------------------------------
  // Layout Constraints
  // ---------------------------------------------------------------------------
  /// Maximum content width to constrain layouts on larger screens.
  static const double maxContentWidth = 800.0;
}
